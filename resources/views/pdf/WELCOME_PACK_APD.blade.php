<h3 style="text-align: center;">MEDICATION ADMINISTRATION/SAFE HANDLING OF MEDICATIONS</h3>
<p><span style="font-size: 10pt;">I recognize the importance of ensuring that medications are dispensed as prescribed and are stored and handled in a safe, approved manner. My guidelines comply with those reflected in a Policy Directive #01-01 &ldquo;Medication administration and Supervision of Self- Administration of Medication&rdquo;.</span></p>
<p><span style="font-size: 10pt;"><strong>TRAINING</strong></span></p>
<p><span style="font-size: 10pt;">I have attended required medication administration training and have been validated on the administration and handling of medications. This training was provided by a Florida licensed nurse and proof of the training is located in my administrative record. This training complied with the requirements outlined in Policy Directive #01-01.</span></p>
<p><span style="font-size: 10pt;"><strong>SAFE STOREGE OF MEDICATION</strong></span></p>
<p><span style="font-size: 10pt;">For individuals who live in their own home all prescription medications will be kept in the container in which it was dispensed with the original labeling attached, unless the person has been determined to be able to self-administer his/her medications and is using a weekly pill container to keep track of his/her medication.</span></p>
<p><span style="font-size: 10pt;"><strong>CONTROLLED DRUGS AND NARCOTICS STORAGE</strong></span></p>
<p><span style="font-size: 10pt;">For individuals who live in their own home and have a roommate, all controlled substances and narcotics are double locked. Only those individuals in supported living arrangements who do not have a roommate and who self-administer their medications do not have to keep medications in the locked storage cabinet as indicated above.</span></p>
<p><span style="font-size: 10pt;"><strong>SUPERVISION ON SELF-ADMINISTRATION OF MEDICATION</strong></span></p>
<p><span style="font-size: 10pt;">Individuals who have been determined capable of self-administration of medications are encouraged to do so. For individuals living in their own home with supported living arrangements, the supervision of self-administration of medication may include:</span></p>
<p><span style="font-size: 10pt;">-Observing the person putting the correct dosage in his/her weekly pill reminder container and providing verbal prompts on the quantity and correct slot in which to put medications.</span></p>
<p><span style="font-size: 10pt;">-Locating the weekly pill reminded container.</span></p>
<p><span style="font-size: 10pt;">- Opening the correct slot in the container and providing verbal instruction regarding which pill to be taken at the time. I understand that I should not remove the medication from its original container or from the pill reminder container. This also does not include preparing syringes for injection or the administrating of injections.</span></p>
<p><span style="font-size: 10pt;">- Observing the individual taking the medication and checking afterward to be sure that the medication has actually been swallowed. I understand I am to not administer medication through a nasal or oral inhaler or through intermittent positive pressure breathing machine or nebulizer. This also administration of peritoneal, irritations or debriding agents, rectal, urethra or vaginal preparation. Supervision of self-administration also does not include prescription medications ordered by a health care practitioner on a PRN or as needed basis where the individual is not legally competent and has not been determined able to make decision about his/her medications.</span></p>
<p><span style="font-size: 10pt;"><strong>DISPOSAL OF MEDICATIONS</strong></span></p>
<p><span style="font-size: 10pt;">Leftover medications should be flushed down the toilet. Prior to completing the process, two staff should be present to witness the procedure. Any changes in medications should result in immediate changes to the medication administration record.</span></p>
